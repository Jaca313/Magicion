#pragma once
#define _CRT_SECURE_NO_WARNINGS
#include <fstream>
#include <string>
#include <chrono>
#include <ctime>
class Info
{
	////////////////////////////////////////////////////////////////////////////////
	//									Init/Core
	////////////////////////////////////////////////////////////////////////////////
private:
	Info() {
	
		INFO.open("Log.txt", std::ofstream::app);
		ERROR.open("Error.txt", std::ofstream::app);
	}
	~Info() {

		INFO.close();
		
	}
public:
	static Info& getInstance() {
		static Info theInstance;
		return theInstance;
	}

	////////////////////////////////////////////////////////////////////////////////
	//									Resources
	////////////////////////////////////////////////////////////////////////////////
private:
	std::ofstream INFO;
	std::ofstream ERROR;



	////////////////////////////////////////////////////////////////////////////////
	//                         Functionality
	////////////////////////////////////////////////////////////////////////////////
private:
	char* GetTime();


public:
	void LOG(std::string message);

	void ERR(std::string error);



};

